#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *) s;
	while (s[i])
		i++;
	while (i > 0)
	{
		if (s[i] == (char) c)
			return (&str[i]);
		i--;
	}
	if (s[i] == (char) c)
		return (&str[i]);
	return (0);
}
